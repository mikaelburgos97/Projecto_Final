    private void limpiarInputs() {
        txt_user.setText("");
        txt_id.setText("");
        txt_password.setText("");
    }

    private void reverseAll() {
        limpiarInputs();
        tbl_user.clearSelection();
        btn_update.setEnabled(false);
        btn_delete.setEnabled(false);
    }

